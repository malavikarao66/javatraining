package com.org.workingwithclasses.staticmembers;

public class Overloading {


    public int add(int a, int b) {
        return a + b;
    }



    public double add(int a, int b, int c) {
        return a + b + c;
    }

    public int add(int a, int b, int c, int d) {
        return a + b + c + d;
    }

}
